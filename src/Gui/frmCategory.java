    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        switch (btnAdd.getText()) {
            case "ADD":
            enableTXTAdd();
            resetTXTAdd();
            btnAdd.setText("SAVE");
            break;
            case "SAVE":
            /*
            if (!validate()) {
                return;
            }
            */
            int countStuff = GUIInteraction.indentityID("select top 1 * from Categories order by ID Desc", "ID") + 1;
            String cateID = "CA"+countStuff;
            entity.Category detail = new entity.Category();
            detail.setCategoryID(cateID);
            detail.setCategoryName(txtTask.getText());
            alDetails.add(detail);
            Categories.insertCategory(detail);
            refresh();
            btnAdd.setText("ADD");
            resetTXTAdd();
            diasbleTXTAdd();
            break;
            default:
            break;
        }
    }//GEN-LAST:event_btnAddActionPerformed

