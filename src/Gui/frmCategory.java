    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        String name = txtTask.getText().trim();
        String id = DataInteraction.getCode("Categories", "Name", name, "ID");
        int i = tableCategory.getSelectedRow();
        if(name.length()==0){
            JOptionPane.showMessageDialog(this, "Please chose one row from table");
            return;
        }
        int j = JOptionPane.showConfirmDialog(this, "Do you want to delete this category.?");
        if (j == JOptionPane.YES_OPTION) {
            interact.Categories.deleteCategory(id);
            alDetails.remove(i);
            refresh();
        }
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        
        if(txtTask.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Please chose one row from table");
            return;
        }
        switch (btnEdit.getText()) {
            case "EDIT":
            txtTask.setEnabled(true);
            btnEdit.setText("SAVE");
            break;
            case "SAVE":
            /*
            if (!validate()) {
                return;
            }
            */
            int i = tableCategory.getSelectedRow();
            String name = String.valueOf(tableCategory.getValueAt(i, 1));
            String id = DataInteraction.getCode("Categories", "Name", name, "ID");
            alDetails.remove(i);
            entity.Category detail = new entity.Category();
            detail.setCategoryID(id);
            detail.setCategoryName(txtTask.getText());
            Categories.editCategory(detail);
            alDetails.add(detail);
            refresh();
            btnEdit.setText("EDIT");
            txtTask.setText(null);
            txtTask.setEnabled(false);
            break;
            default:
            break;
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void refresh() {
        tableCategory.setModel(new model.CategoryModel(alDetails));
        setWithTable();
        try {
            txtCountItem.setText(String.valueOf(GUIInteraction.countRecord("select * from Categories")));
//            GUIInteraction.readToTable("select * from Stuffs", tableStuff);
        } catch (SQLException ex) {
            Logger.getLogger(frmStuff.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
